        public async Task WriteModificationAsync(string filePath, string modifiedCode)
        {
            bool isSafe = await IsFileSafeToModifyAsync(filePath);

            if (isSafe)
            {
                await File.WriteAllTextAsync(filePath, modifiedCode);
            }

            await Task.CompletedTask;
        }

        public async Task<bool> IsFileSafeToModifyAsync(string filePath)
        {
            bool isNullOrEmpty = string.IsNullOrWhiteSpace(filePath);

            if (isNullOrEmpty)
            {
                return await Task.FromResult(false);
            }

            bool isProtected = false;

            foreach (var protectedPath in _protectedPaths)
            {
                bool pathStartsWithProtected = filePath.StartsWith(protectedPath, StringComparison.OrdinalIgnoreCase);

                if (pathStartsWithProtected)
                {
                    isProtected = true;
                    break;
                }
            }

            bool isSafe = !isProtected;
            return await Task.FromResult(isSafe);
        }

        public async Task<string> CreateBackupAsync(string filePath)
        {
            bool fileExists = File.Exists(filePath);

            if (fileExists)
            {
                string fileName = Path.GetFileName(filePath);
                string timestamp = DateTime.Now.ToString("yyyy-MM-dd_HH-mm-ss");
                string backupPath = Path.Combine(_backupPath, $"{fileName}.{timestamp}.bak");

                await File.WriteAllTextAsync(backupPath, await File.ReadAllTextAsync(filePath));
                return backupPath;
            }
            else
            {
                return string.Empty;
            }
        }
    }
