using Alissa.Core.Interfaces;

namespace Alissa.Core.Services
{
    /// <summary>
    /// Service for integrating Alissa into external programs and modifying code.
    /// </summary>
    public class CodeIntegrationService : ICodeIntegrationService
    {
        private readonly string _basePath;
        private readonly string _backupPath;
        private readonly HashSet<string> _protectedPaths;

        public CodeIntegrationService(string basePath)
        {
            _basePath = basePath;
            _backupPath = Path.Combine(basePath, "backups");
            _protectedPaths = new HashSet<string>
            {
                Path.Combine(basePath, "config"),
                Path.Combine(basePath, "memory"),
                Path.Combine(basePath, "logs")
            };

            Directory.CreateDirectory(_backupPath);
        }

        public async Task<string> ReadCodeFileAsync(string filePath)
        {
            bool fileExists = File.Exists(filePath);

            if (fileExists)
            {
                string content = await File.ReadAllTextAsync(filePath);
                return content;
            }
            else
            {
                return string.Empty;
            }
        }

